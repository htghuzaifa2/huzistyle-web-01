// Helper function to get a blog by slug
export function getBlogBySlug(slug) {
  return blogs.find(blog => blog.slug === slug) || null;
}

// Helper function to get blogs by category
export function getBlogsByCategory(category) {
  return blogs.filter(blog => blog.category === category);
}

// Helper function to get all unique categories
export function getBlogCategories() {
  return [...new Set(blogs.map(blog => blog.category))];
}

// Helper function to get related blogs (same category, excluding current)
export function getRelatedBlogs(currentSlug, limit = 3) {
  const current = getBlogBySlug(currentSlug);
  if (!current) return [];
  return blogs
    .filter(blog => blog.slug !== currentSlug && blog.category === current.category)
    .slice(0, limit);
}
